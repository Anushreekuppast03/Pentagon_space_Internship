// Math Object
// Math is an inbuilt object in JavaScript which we can use
// to perform some specific math operations
console.log(Math.PI)                 //PRINT PI VALUE
console.log(Math.sqrt(49));         // prints square root of 49
console.log(Math.pow(2, 3));        // prints 2 raised to power 3
console.log(Math.floor(5.8));       // returns largest integer ≤ 5.8
console.log(Math.ceil(5.2));        // returns smallest integer ≥ 5.2
console.log(Math.round(4.6));       // rounds to nearest integer
console.log(Math.max(10, 20, 30));  // returns maximum value
console.log(Math.min(10, 20, 30));  // returns minimum value
console.log(Math.abs(-15));         // returns absolute value (15)
console.log(Math.sign(-8760)); 
console.log(Math.sign(23456));       // returns sign: -1 for negative, 1 for positive
console.log(Math.random());         // generates random number between 0 and 1
